class Solution:
    #Function to count the number of ways in which frog can reach the top.
    def countWays(self,n):
        
        # code here
        d = {0:1,1:1,2:2}
        for i in range(3,n+1):
            d[i] = d[i-1]+d[i-2]+d[i-3]
        return d[n]%(10**9+7)

